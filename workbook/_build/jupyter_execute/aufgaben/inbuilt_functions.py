# Eingebaute Funktionen, auch built-in Functions genannt, sind Funktionen die ein fester Bestandteil in Python sind.
# 
# Sie helfen immer wieder kehrende Aufgaben schnell und ohne selbstgeschriebenen Code zu lösen.  
# 
# 
# Zu den eingebauten Funktionen die in den Grundlagen behandelt werden gehören:
# 
# 
# | Funktion  | Beschreibung  | 
# |----------|:-------------  |
# | help() | zeigt detailierte Informationen zu einer Funktion an |
# | dir()  | zeigt alle Attribute und Methoden eines Objektes an |
# | print()| druckt / gibt eine Nachricht oder einen Wert auf dem Bildschirm aus |
# | type() | gibt den Datentyp einer Variable zurück |
# | input()| nimmt die Eingabe eines:r Nutzer:in entgegen und gibt diesen als string zurück |
# 
# 

