# # Operatoren 
# 
# Python kann als einfacher Taschenrechner verwendet werden. Dabei helfen artithmetische Operatoren:
# 
# | Operatoren  | Beschreibung  | 
# |----------|:-------------  |
# | + |  Addition |
# | - |  Subtraktion|
# | / |  Division |
# | * |  Mulitplikation |
# | // |  Ganzzahl-Division |
# | % |  Modulo - Rest der Division|
# | ** |  Potenzierung |
# 
# 
# 
# Bei der Ausführung wird die Rechenregel "Punkt vor Strich" eingehalten.
# Mit Klammern können Ausdrücke gruppiert werden.
# 

# In[1]:


# Addition
5 + 6


# In[2]:


# Subtraktion
20 - 4


# In[3]:


# Multiplikation
2 * 6


# In[4]:


# Division
9 / 2


# In[5]:


# Ganzzahl-Division
9 // 2


# In[6]:


# Modulo
9 % 2


# In[7]:


# Potenzieren
2 ** 3


# In[8]:


# Rechenreihenfolge 
4 + 2 * 3


# In[9]:


# Mit Klammern
(4 + 2) * 3

